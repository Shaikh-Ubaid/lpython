def run_cmd(cmd, cwd=None):
    global cmd_output, cmd_error
    process = sp.run(cmd, shell=True, cwd=cwd, capture_output=True, text=True)
    cmd_output += "\n" + process.stdout
    cmd_error += "\n" + process.stderr
    if process.returncode != 0:
        return False
    return True


