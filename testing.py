    print(f"Test {idx}: ")
    extra_options = []
    labels = test["LABELS"].split(";")
    if test['FAIL']:
        extra_options.append("FAIL")

    if "llvm" in labels:
        cmd = f"lpython --fast integration_tests/{test['NAME']}.py"
        if not run_cmd(cmd) and test['EXTRAFILES'] == '' and test['IMPORT_PATH'] == '' and (not test['FAIL']):
            failed_tests.append(test['NAME'])
            extra_options.append("NOFAST")

    clean_output += f"RUN(NAME {test['NAME']} " + \
        " ".join(extra_options) + "\n"
    clean_output += "    " + "LABELS " + " ".join(labels) + "\n"
    if test["IMPORT_PATH"]:
        clean_output += "    " + "IMPORT_PATH " + test['IMPORT_PATH'] + "\n"
